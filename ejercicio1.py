#Dado los valores ingresados por un usuario (base,, altura), calcular y mostrar en pantalla el area de un triangulo

print("Por Favor Ingrese la medida de la base del triangulo:")
base=int(input())
print("Por favor ingrese la altura del triangulo:")
altura=int(input())

area=float((base*altura)/2)
print("El area del triangulo es: ",area)